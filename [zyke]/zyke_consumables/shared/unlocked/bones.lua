---@type table<string, number>
local lookup = {}
for i = 1, #Config.Bones do
	lookup[tostring(Config.Bones[i].id)] = i
end

---@param boneId number
---@param action "fromInventory" | "toInventory" | "fromGround" | "toGround"
---@return {dict: string, clip: string, duration: integer, despawnProps: integer} | nil
function GetBoneAnimation(boneId, action)
	local boneData = Config.Bones[lookup[tostring(boneId)]]
	if not boneData then return nil end

	return boneData.animations[action]
end

